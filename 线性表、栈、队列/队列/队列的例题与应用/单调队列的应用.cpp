/*
单调队列：
    单调队列是一种特殊的数据结构，队列中的元素保持单调递增或单调递减的顺序。
    主要用于在一个滑动窗口内维护元素的单调性，以便快速获取窗口内的最大值或最小值。
    单调队列通常使用双端队列（deque）来实现。
*/

/*
题目:
n个数的数列，从左至右输出每个长度为m的区间内的最小值。

输入要求:第一行输入两个正整数n和m，第二行输入这n个数，相邻数用空格隔开
如:
6 3
1 2 5 3 4 6

输出要求:依次输出每个长度为m的区间内的最小值，相邻数用空格隔开
如:
1 2 3 3
*/
// 解题核心思想：如果一个新来的元素比你小，并且比你晚离开窗口，那么你永远不可能成为最小值了，你就可以被淘汰了

// 单调队列还有一些其他应用，如优化动态规划，解决一些区间问题等。通过维护一个单调队列，可以在O(n)的时间复杂度内解决一些原本需要O(n^2)的暴力解法的问题。
// 此题运用单调队列的特殊点还在于此题运用了双端队列的特性，既要在队尾入队，又要在队头出队，来维护一个滑动窗口内的最小值。
#include <iostream>
#include <vector>
#include <deque>

using namespace std;

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n, m;
    if (!(cin >> n >> m))
        return 0;

    vector<int> a(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }

    // 双端队列，用来存储元素的【下标】而不是直接存数值
    // 存下标的好处是可以方便地判断该元素是否已经滑出了当前窗口
    deque<int> dq;

    for (int i = 0; i < n; i++)
    {
        // 1. 去头（淘汰过期元素）：
        // 判断队头元素是否已经滑出了当前窗口 [i-m+1, i]
        // 即判断队头的下标是否小于或等于 i - m
        if (!dq.empty() && dq.front() <= i - m)
        {
            dq.pop_front();
        }
        // 2. 去尾（维护单调性）：
        // 保持队列对应元素严格单调递增。
        // 如果当前新来的元素 a[i] 比队尾对应的元素小或相等，
        // 那么队尾元素就失去竞争最小值的资格，直接淘汰。
        while (!dq.empty() && a[dq.back()] >= a[i])
        {
            dq.pop_back();
        }
        // 3. 进队：
        // 将当前元素的下标从队尾加入队列
        dq.push_back(i);
        // 4. 取值：
        // 当窗口完全形成时（即处理到第 m-1 个元素及以后），开始输出最小值
        if (i >= m - 1)
        {
            cout << a[dq.front()];
            if (i < n - 1)
            {
                cout << " ";
            }
        }
    }

    cout << "\n";

    return 0;
}